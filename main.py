# 2. Работа со словарями
my_dict = {"Павел": 1991, "Евгения": 1995, "Виктор": 1985}

# 2.1. Вывод на экран
print("Словарь my_dict:", my_dict)

# 2.2. Вывод значений по ключу
print("Год рождения Павла:", my_dict["Павел"])  # Существующий ключ
print("Год рождения Ильи:", my_dict.get("Илья", "Ключ не найден"))  # Отсутствующий ключ

# 2.3. Добавление новых пар
my_dict["Владимир"] = 2000
my_dict["Алексей"] = 1988

# 2.4. Удаление пары и вывод значения
deleted_value = my_dict.pop("Евгения")
print("Удаленное значение:", deleted_value)

# 2.5. Вывод измененного словаря
print("Измененный словарь my_dict:", my_dict)

# 3. Работа с множествами
my_set = {1, 2, "яблоко", 7.2, 2, "яблоко", True, False}

# 3.1. Вывод множества на экран
print("Множество my_set:", my_set)

# 3.2. Добавление элементов
my_set.add("вишня")
my_set.add(42)

# 3.3. Удаление элемента
my_set.remove(7.2)

# 3.4. Вывод измененного множества
print("Измененное множество my_set:", my_set)